def randomize_options(question_tuple):
    question, opt1, opt2, opt3, opt4, correct, explanation = question_tuple
    options = [opt1, opt2, opt3, opt4]
    correct_index = ord(correct) - ord('A')  # 0 for A, 1 for B, etc.
    correct_option = options[correct_index]
    random.shuffle(options)
    new_correct_index = options.index(correct_option)
    new_correct = chr(ord('A') + new_correct_index)
    return (question, options[0], options[1], options[2], options[3], new_correct, explanation)

